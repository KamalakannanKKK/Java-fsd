package Project1;

public class typecasting {

	public static void main(String[] args) {
		//implicit
				byte a=5;
				int b=a;
				long c=b;
				System.out.print(a);
				System.out.print(b);
				System.out.print(c);
				
				// explicit
				char m=94;
				int n=(int)m;
				long o=(long)n;
				System.out.print(m);
				System.out.print(n);
				System.out.print(o);
				

	}

}
